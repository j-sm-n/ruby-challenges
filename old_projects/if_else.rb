weather = 'sunny'

case weather
when 'cloudy'
	puts "Better bring an umbrella with you just in case!"
when 'rainy'
	puts "Oh gosh, it's golosh time!"
when 'snowing'
	puts "Why are you even out? It must be FREEZING!"
when 'sunny'
	puts "Crack open a cold one and break out the short sleeves, it's sunny out."
when 'raining cats & dogs'
	puts "Duck and cover!"
else 
	puts "Hmm, well I don't know what to tell you."
end